    public BarcodeFrame() {
        initComponents();
        this.setResizable(false);
        proPriceTable.setModel(productPriceTable);
        List<ProductInfo> list = op.getProductsList();
        productPriceTable.addColumn("Name");
        productPriceTable.addColumn("Id");
        productPriceTable.addColumn("Quantity");
        productPriceTable.addColumn("Price");
        addedList.setModel(addList);

        for (ProductInfo i : list) {
            productPriceTable.addRow(new Object[]{i.getProductName(), i.getProductId(), i.getQuantity(), i.getPrice()});
        }
    }

    public void update() {
        List<ProductInfo> list = op.getProductsList();
        for (int i = 0; i < list.size(); i++) {
            productPriceTable.removeRow(0);
            productPriceTable.addRow(new Object[]{list.get(i).getProductName(), list.get(i).getProductId(), list.get(i).getQuantity(), list.get(i).getPrice()});
        }

    }

