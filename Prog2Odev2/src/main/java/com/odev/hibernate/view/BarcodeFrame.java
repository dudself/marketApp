    private void addProButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProButtonActionPerformed
        int id = Integer.valueOf(prodIdField.getText());
        boolean foo = op.updateProductCount(id, "decrease", 0);
        ProductInfo pi = op.getProduct(id);
        if (foo) {
            addList.add(addList.getSize(), pi.getProductName());
            update();
            money = money + pi.getPrice();
            moneyLabel.setText(money + "TL");
        }
        

    }//GEN-LAST:event_addProButtonActionPerformed
//seçilemleri silme butonu
    private void deleteSelectedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSelectedButtonActionPerformed
        if (addedList.getSelectedValue() != null) {
            op.updateProductCount(op.getIdByProductName(addedList.getSelectedValue()), "increase",0);
            money = money - op.getProduct(op.getIdByProductName(addedList.getSelectedValue())).getPrice();
            addList.removeElement(addedList.getSelectedValue());
            moneyLabel.setText(String.valueOf(money) + " TL");
            update();
        } else
            JOptionPane.showMessageDialog(this, "Selected Product is non-existent. "); 
    }//GEN-LAST:event_deleteSelectedButtonActionPerformed
//hepsini silme butonu
    private void delAllProButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delAllProButtonActionPerformed
        money = 0;
        moneyLabel.setText(String.valueOf(money) + " TL");
        addList.removeAllElements();
    }//GEN-LAST:event_delAllProButtonActionPerformed
